public class VarInt
{
  public static void main(String[] args)
  {
    // Declaration
    int n1, n2;

    // Assignment
    n1 = 7;
    n2 = 13;

    // Addition
    System.out.println("7 + 13 = " + (n1 + n2));

    // Subtraction
    System.out.println("7 - 13 = " + (n1 - n2));

    // Multiplication
    System.out.println("7 * 13 = " + (n1 * n2));

    // Division
    System.out.println("7 / 13 = " + (n1 / n2));

    // Modulus
    System.out.println("7 % 13 = " + (n1 % n2));
    
  }
}
