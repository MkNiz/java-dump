public class TypeCast
{
  public static void main(String[] args)
  {
    // Create initial double
    double myMoney = 17.25;

    // Display as a double
    System.out.println("I have $" + myMoney + ".");

    // Convert to an int, and display that int
    System.out.println("That means my dollar bills are worth " + (int)myMoney + " dollars.");
  }
}
