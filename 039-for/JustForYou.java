public class JustForYou
{
  public static void main(String[] args)
  {
    int nJorts;
    for (nJorts = 1; nJorts <= 5; nJorts++) {
      System.out.println("You buy a pair of jorts.");
      System.out.println("You now have " + nJorts + " jorts.\n");
    }
    System.out.println("There are no more jorts anywhere, you already have them all.");
  }
}
