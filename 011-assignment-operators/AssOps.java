public class AssOps
{
  public static void main(String[] args)
  {
    int n = 1;
    System.out.println("n = " + n);
    n += 3;
    System.out.println("n += 3 = " + n);
    n -= 2;
    System.out.println("n -= 2 = " + n);
    n *= 5;
    System.out.println("n *= 5 = " + n);
    n /= 2;
    System.out.println("n /= 2 = " + n);
    n %= 1;
    System.out.println("n %= 1 = " + n);
  }
}
