public class IfElse
{
  public static void main(String[] args)
  {
    int jorts = 20;
    if (jorts >= 12)
      System.out.println("You have far too many jorts. An excessive number of jorts.");
    else
      System.out.println("You have enough jorts. Please, for the love of God, do not get more.");
  }
}
