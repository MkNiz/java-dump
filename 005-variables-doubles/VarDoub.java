public class VarDoub
{
  public static void main(String[] args)
  {
    // Declaration
    double d1, d2;

    // Assignment
    d1 = 4.54;
    d2 = 121.23;

    // Addition
    System.out.println(d1 + " + " + d2 + " = " + (d1 + d2));

    // Subtraction
    System.out.println(d1 + " - " + d2 + " = " + (d1 - d2));

    // Multiplication
    System.out.println(d1 + " * " + d2 + " = " + (d1 * d2));

    // Division
    System.out.println(d1 + " / " + d2 + " = " + (d1 / d2));

    // Modulus
    System.out.println(d1 + " % " + d2 + " = " + (d1 % d2));
  }
}
